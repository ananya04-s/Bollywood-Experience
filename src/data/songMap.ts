export function getMovieSongs(movieId: string, movieTitle?: string): string[] {
  // Try matching by standard id
  if (MOVIE_SONGS_MAP[movieId]) {
    return MOVIE_SONGS_MAP[movieId];
  }

  // Try parsing by matching some word in title
  if (movieTitle) {
    const titleLower = movieTitle.toLowerCase();
    for (const [key, list] of Object.entries(MOVIE_SONGS_MAP)) {
      if (titleLower.includes(key.split("-")[0])) {
        return list;
      }
    }
  }

  // Beautiful fallback song suggestions matching generic fun soundtracks
  return [
    `${movieTitle ? movieTitle + ' ' : ''}Main Soundtrack`,
    "Dancing Beats Reprise",
    "Acoustic Love Prelude"
  ];
}
